
SELECT 
  SUC.VCH_SUCUNOMBRE SUCURSAL,
  MONED.VCH_MONEDESCRIPCION MONEDA, 
  SUM(CUE.DEC_CUENSALDO) SALDO, 
  COUNT(1) CUENTAS
FROM EUREKA.CUENTA CUE 
JOIN EUREKA.MONEDA MONED
ON CUE.CHR_MONECODIGO = MONED.CHR_MONECODIGO
JOIN EUREKA.SUCURSAL SUC
ON CUE.CHR_SUCUCODIGO = SUC.CHR_SUCUCODIGO
GROUP BY SUC.VCH_SUCUNOMBRE, MONED.VCH_MONEDESCRIPCION
ORDER BY 1,2;


 